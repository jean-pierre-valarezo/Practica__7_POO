    private void actualizarTabla() {
    List<Disco> discos = controladorCantante.verDiscos();
 
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Anio de Lanzamiento");

        for (Disco disco : discos) {
            Object[] fila = {disco.getCodigo(), disco.getNombre(), disco.getAnioLanzamiento()};
            modelo.addRow(fila);
        }

        jTable1.setModel(modelo);
    }

